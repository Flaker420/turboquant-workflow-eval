#!/usr/bin/env python3
"""Generate the CLI reference tables in README.md from argparse metadata.

Usage:
    python scripts/generate_cli_docs.py            # rewrite README.md in place
    python scripts/generate_cli_docs.py --check    # exit non-zero on drift

The generator walks every argument on
``turboquant_workflow_eval.__main__._build_parser`` and renders one markdown
table per argparse group, replacing the region of README.md delimited by
``<!-- cli-docs:start -->`` / ``<!-- cli-docs:end -->`` sentinels.
"""
from __future__ import annotations

import argparse
import sys
from pathlib import Path

# Ensure `src/` is importable when invoked directly.
_ROOT = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(_ROOT / "src"))

from turboquant_workflow_eval.__main__ import _build_parser  # noqa: E402

START = "<!-- cli-docs:start -->"
END = "<!-- cli-docs:end -->"


def _format_type(action: argparse.Action) -> str:
    if action.choices:
        return "{" + ",".join(str(c) for c in action.choices) + "}"
    if isinstance(action, argparse._StoreTrueAction):
        return "flag"
    if isinstance(action, argparse._StoreFalseAction):
        return "flag"
    if isinstance(action, argparse._AppendAction):
        base = getattr(action.type, "__name__", "str") if action.type else "str"
        return f"{base} (repeatable)"
    if action.type is None:
        return "str"
    return getattr(action.type, "__name__", str(action.type))


def _format_default(action: argparse.Action) -> str:
    if action.default in (None, argparse.SUPPRESS):
        return "—"
    if action.default == []:
        return "—"
    if isinstance(action, argparse._StoreTrueAction):
        return "False"
    if isinstance(action, argparse._StoreFalseAction):
        return "True"
    return f"`{action.default!r}`"


def _format_flag(action: argparse.Action) -> str:
    opts = [o for o in action.option_strings if o.startswith("--")] or action.option_strings
    if not opts:
        return ""
    main = opts[0]
    if action.metavar:
        return f"`{main} {action.metavar}`"
    if isinstance(action, (argparse._StoreTrueAction, argparse._StoreFalseAction)):
        return f"`{main}`"
    dest = action.dest.upper().replace("_", "-")
    return f"`{main} {dest}`"


def _iter_groups(parser: argparse.ArgumentParser):
    """Yield (title, [actions]) for each group, preserving declaration order."""
    for group in parser._action_groups:
        actions = [
            a
            for a in group._group_actions
            if a.option_strings and not isinstance(a, argparse._HelpAction)
        ]
        if not actions:
            continue
        yield group.title or "options", actions


def _render_group(title: str, actions: list[argparse.Action]) -> str:
    lines = [f"### {title}", "", "| Flag | Type | Default | Description |",
             "|------|------|---------|-------------|"]
    for a in actions:
        flag = _format_flag(a)
        ty = _format_type(a)
        default = _format_default(a)
        help_text = (a.help or "").replace("\n", " ").replace("|", "\\|")
        lines.append(f"| {flag} | {ty} | {default} | {help_text} |")
    lines.append("")
    return "\n".join(lines)


def render() -> str:
    parser = _build_parser()
    blocks = [
        "<!-- Auto-generated by scripts/generate_cli_docs.py. Do not edit by hand. -->",
        "",
    ]
    for title, actions in _iter_groups(parser):
        blocks.append(_render_group(title, actions))
    return "\n".join(blocks).rstrip() + "\n"


def rewrite(readme_path: Path, *, check: bool) -> int:
    text = readme_path.read_text()
    if START not in text or END not in text:
        print(f"error: README is missing {START}/{END} sentinels", file=sys.stderr)
        return 2
    head, _, rest = text.partition(START)
    _, _, tail = rest.partition(END)
    generated = render()
    new_text = f"{head}{START}\n{generated}{END}{tail}"
    if check:
        if new_text != text:
            print("README CLI docs are stale. Run: python scripts/generate_cli_docs.py",
                  file=sys.stderr)
            return 1
        return 0
    if new_text != text:
        readme_path.write_text(new_text)
        print(f"wrote {readme_path}")
    else:
        print("README is already up to date")
    return 0


def main() -> int:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--check", action="store_true",
                    help="Exit non-zero if README is out of date.")
    ap.add_argument("--readme", type=Path, default=_ROOT / "README.md")
    args = ap.parse_args()
    return rewrite(args.readme, check=args.check)


if __name__ == "__main__":
    raise SystemExit(main())
